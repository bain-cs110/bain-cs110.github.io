# Arithmetic Operators #
########################

# Numbers #########
print(10 + 5)   # addition
print(10 - 5)   # subtraction
print(10 * 5)   # multiplication
print(10 / 5)   # division (notice the result is a float)
print(11 % 5)   # modulus  11 / 5 = 2 with 1 left over (remainder = 1)
print(11 // 5)  # quotient  11 / 5 = 2 with 1 left over (quotient = 2)
print(2 ** 3)  # exponent
print(4 ** (1/2))  # square root (notice the result is a float)

# Strings #########
print("abc" + "defg")  # in the context of a string, the + sign concatenates two strings

print("Hello" + "there") # Don"t forget...it won"t add a space unless you tell it to!
print("Hello " + "there")


